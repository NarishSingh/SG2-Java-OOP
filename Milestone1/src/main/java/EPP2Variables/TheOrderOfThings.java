public class TheOrderOfThings {

    public static void main(String[] args) {
        double number = 5.0;
        String opinion, size, age, shape, color, origin, material, purpose;
        String noun = "dragons";

        opinion = "AWESOME";
        size = "teensy-weensy";
        age = "new";
        shape = "oblong";
        color = "yellow";
        origin = "Martian";
        material = "platinum";
        purpose = "good";

        System.out.print(number + " " + opinion + " " + size + " " + age + " " + shape);
        System.out.println(" " + color + " " + origin + " " + material + " " + purpose + " " + noun);
    }

}
